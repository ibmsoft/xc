package com.cubedrive.sheet.domain.sheet;

import com.cubedrive.base.domain.BaseDomain;
import com.cubedrive.base.domain.account.User;

public class SheetCustom extends BaseDomain {
	
	private static final long serialVersionUID = -2512859314186445811L;

	private Integer id;

	private User user;
	
	private String category;

	private String content;

    public SheetCustom() {
    }

    public SheetCustom(User user, String category, String content) {
        this.user=user;
        this.category = category;
        this.content = content;
    }

    public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		SheetCustom other = (SheetCustom) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

}
